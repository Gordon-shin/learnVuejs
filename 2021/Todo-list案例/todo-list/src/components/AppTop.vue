<template>
  <div class="todo-header">
    <input type="text" placeholder="请输入你的任务名称，按回车键确认" v-model="title" @keyup.enter="add"/>
  </div>
</template>

<script>

import {nanoid} from 'nanoid'
export default {
  name: "AppTop",
  //data是方法
  data(){
    return {
      title:''
    }
  },

  methods:{
    add(e){
      console.log(e.target)
      console.log(this.title);
      if (!this.title.trim()){
        alert('代办事项不能为空')
        return;
      }
      const todoObj={
        id:nanoid(),
        title:this.title,
        done:false
      }
      this.addTodo(todoObj);
      this.title='';
    }

  },
  //props 用来存储父组件发过来的参数的,属性名需要带''
  props: ['addTodo'],
}
</script>

